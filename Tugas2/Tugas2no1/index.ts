//penjumlahan
function penjumlahan(q:number, w:number, e:number):number {
    return q + w + e
}
console.log(penjumlahan(3,6,9))
//pengurangan
function pengurangan(r:number, t:number, y:number):number {
    return r - t - y
}
console.log(pengurangan(10,5,2))

//perkalian
function perkalian(u:number, i:number, o:number):number {
    return u * i * o
}
console.log(perkalian(7,8,6))

